function sum(a, b) {
  return a + b;
}
function multiply(a, b) {
  return a * b;
}
function subtract(a, b) {
  return a - b;
}
function divide(a, b) {
  return a / b;
}
const ans = sum(1, 2);
console.log(ans);

const ans1 = multiply(1, 2);
console.log(ans1);

const ans2 = subtract(1, 2);
console.log(ans2);

const ans3 = divide(1, 2);
console.log(ans3);
